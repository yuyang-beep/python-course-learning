# ============================================================
# 三、列表运算
# ============================================================

a = [1, 2, 3]
b = [4, 5, 6]

print(a + b)        # 拼接 → [1, 2, 3, 4, 5, 6]
print(a * 3)        # 重复 → [1, 2, 3, 1, 2, 3, 1, 2, 3]
print(3 in a)       # 成员判断 → True
print(9 not in a)   # → True


# ============================================================
# 四、索引：访问单个元素
# ============================================================
# 正向索引：从 0 开始
# 反向索引：从 -1 开始（-1 是最后一个）

fruits = ['apple', 'banana', 'pitaya', 'peach', 'watermelon']
#索引:       0         1        2         3          4
#反向:      -5        -4       -3        -2         -1

print(fruits[0])    # apple（第1个）
print(fruits[2])    # pitaya（第3个）
print(fruits[-1])   # watermelon（最后一个）
print(fruits[-2])   # peach（倒数第2个）

fruits[2] = 'durian'   # 修改第3个元素
print(fruits)


# ============================================================
# 五、切片：一次访问多个元素 [start:end:step]
# ============================================================
# 规则：取 start 到 end-1 的元素，按 step 跨度
# start/end/step 都可以省略

items = ['a', 'b', 'c', 'd', 'e']
#         0    1    2    3    4

print(items[1:3])    # ['b', 'c']（取索引1和2，不含3）
print(items[:3])     # ['a', 'b', 'c']（从头到索引2）
print(items[2:])     # ['c', 'd', 'e']（从索引2到末尾）
print(items[::2])    # ['a', 'c', 'e']（每隔一个取一个）
print(items[::-1])   # ['e', 'd', 'c', 'b', 'a']（反转列表！）


# ============================================================
# 六、遍历列表（两种方式）
# ============================================================

langs = ['Python', 'Java', 'C++', 'Go']

# 方式1：通过索引
for i in range(len(langs)):
    print(f'第{i+1}个：{langs[i]}')

print()

# 方式2：直接遍历元素（更 Pythonic，推荐）
for lang in langs:
    print(lang)


