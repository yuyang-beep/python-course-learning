# ============================================================
# 一、为什么需要列表？
# ============================================================
# 问题：统计掷骰子6000次，每种点数出现几次？
# 旧方法：6个变量 f1 f2 f3 f4 f5 f6 → 丑陋且难扩展
# 新方法：1个列表搞定

import random

counters = [0] * 6          # 创建6个0：[0, 0, 0, 0, 0, 0]
for _ in range(6000):
    face = random.randrange(1, 7)
    counters[face - 1] += 1 # 摇出1点 → counters[0]+1，以此类推

for face in range(1, 7):
    print(f'{face}点出现了{counters[face - 1]}次')


# ============================================================
# 二、创建列表
# ============================================================

nums   = [35, 12, 99, 68, 55]       # 数字列表
