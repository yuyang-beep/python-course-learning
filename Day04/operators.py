# ============================================================
# 一、算术运算符
# ============================================================

print(10 + 3)   # 加法 → 13
print(10 - 3)   # 减法 → 7
print(10 * 3)   # 乘法 → 30
print(10 / 3)   # 除法 → 3.333...（结果是小数）
print(10 // 3)  # 整除 → 3（去掉小数部分）
print(10 % 3)   # 取余 → 1（10 除以 3 余 1）
print(2 ** 8)   # 幂运算 → 256（2的8次方）


# ============================================================
# 二、运算优先级（先乘除后加减，括号最优先）
# ============================================================

print(2 + 3 * 5)        # → 17（先算乘法）
print((2 + 3) * 5)      # → 25（括号优先）
print(2 ** 3 + 1)       # → 9（先算幂）


# ============================================================
# 三、赋值运算符（复合赋值）
# ============================================================

score = 100
score += 10    # 等同于 score = score + 10
print(score)   # → 110

score -= 5     # 等同于 score = score - 5
print(score)   # → 105

score *= 2     # 等同于 score = score * 2
print(score)   # → 210


# ============================================================
# 四、比较运算符（结果是 True 或 False）
# ============================================================

print(3 > 2)    # → True
print(3 < 2)    # → False
print(3 == 3)   # → True（两个等号才是"相等"）
print(3 != 2)   # → True（不等于）
print(3 >= 3)   # → True


# ============================================================
# 五、逻辑运算符
# ============================================================

# and：两个条件都为 True，结果才是 True
print(3 > 2 and 5 > 1)   # → True
print(3 > 2 and 5 < 1)   # → False

# or：有一个条件为 True，结果就是 True
print(3 > 2 or 5 < 1)    # → True
print(3 < 2 or 5 < 1)    # → False

# not：取反
print(not True)   # → False
print(not False)  # → True


# ============================================================
# 六、实战：用 input() 接收用户输入
# ============================================================

# 华氏温度转摄氏温度
# 公式：C = (F - 32) / 1.8
f = float(input('请输入华氏温度：'))
c = (f - 32) / 1.8
print(f'{f:.1f} 华氏度 = {c:.1f} 摄氏度')


# ============================================================
# 【练习】：判断闰年
# 规则：能被4整除且不能被100整除，或者能被400整除
# ============================================================

year = int(input('请输入一个年份：'))
is_leap = year % 4 == 0 and year % 100 != 0 or year % 400 == 0
print(f'{year} 年是闰年：{is_leap}')
