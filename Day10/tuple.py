# ============================================================
# 一、为什么需要元组？
# ============================================================
# 元组（tuple）和列表很像：都能按顺序存一堆数据，都支持索引、切片、遍历
# 最大区别：元组一旦创建就不能修改（不可变），列表可以随意增删改
# 适合存"不应该被改动"的数据：比如一个坐标点、一条已生成的记录、RGB颜色等


# ============================================================
# 二、创建元组
# ============================================================

t1 = (1, 2, 3)              # 用 () 创建
t2 = 1, 2, 3                 # 括号可以省略，效果一样
t3 = (5,)                    # 只有1个元素时，逗号不能省
t4 = ()                       # 空元组
t5 = tuple([1, 2, 3])         # 用 tuple() 把列表转成元组

print(t1, type(t1))          # (1, 2, 3) <class 'tuple'>
print(t2, type(t2))          # (1, 2, 3) <class 'tuple'>
print(t3, type(t3))          # (5,) <class 'tuple'>
print(type((5)))             # <class 'int'> —— 注意！不加逗号就只是普通括号，不是元组


# ============================================================
# 三、元组的不可变性
# ============================================================

langs = ('Python', 'Java', 'C++')
# langs[0] = 'Go'     # ❌ TypeError: 'tuple' object does not support item assignment
# langs.append('Go')  # ❌ AttributeError: 元组没有 append 方法

print(langs)          # ('Python', 'Java', 'C++')


# ============================================================
# 四、元组支持的操作（和列表用法一致的部分）
# ============================================================

nums = (10, 20, 30, 40, 50)

# 索引和切片
print(nums[0])        # 10
print(nums[-1])       # 50
print(nums[1:3])      # (20, 30)
print(nums[::-1])     # (50, 40, 30, 20, 10)

# 运算符
a = (1, 2)
b = (3, 4)
print(a + b)          # 拼接 → (1, 2, 3, 4)
print(a * 2)          # 重复 → (1, 2, 1, 2)
print(3 in b)         # 成员判断 → True

# 常用方法（只有这两个，因为元组不能增删改）
print(nums.index(30))  # 第一次出现的位置 → 2
print(nums.count(20))  # 出现次数 → 1

# 遍历
for n in nums:
    print(n, end=' ')
print()


# ============================================================
# 五、元组拆包（重要！一行同时给多个变量赋值）
# ============================================================

point = (3, 5)
x, y = point          # 拆包：元组的元素依次赋值给 x, y
print(x, y)           # 3 5

# 交换两个变量的值——不需要中间变量
a, b = 10, 20
a, b = b, a           # 本质就是元组拆包：(a, b) = (b, a)
print(a, b)           # 20 10

# 用 * 收集多余的元素（结果是列表，不是元组）
first, *rest = (1, 2, 3, 4, 5)
print(first, rest)    # 1 [2, 3, 4, 5]


# ============================================================
# 六、列表 vs 元组，怎么选？
# ============================================================
# 会变化的数据（购物车、待办清单……）        → 用列表 list
# 不该被改动的数据（坐标、生日、颜色……）      → 用元组 tuple
# 元组比列表更省内存、访问略快，还能向读代码的人表达"这批数据不会变"的意图


# ============================================================
# 七、实战：给双色球号码"开奖封盘"
# 复习 Day09 的双色球随机选号：这次每一注生成后用元组保存，
# 表示"号码已经定了，不能再被误改"；多注结果仍然放在一个列表里
# （列表本身可以继续增删，但每一注内部的号码不可变）
# ============================================================

import random

n = int(input('\n生成几注双色球号码：'))
red_balls  = list(range(1, 34))
blue_balls = list(range(1, 17))

records = []   # 存放所有注，列表本身可变
for i in range(n):
    red = tuple(sorted(random.sample(red_balls, 6)))  # 转成元组：这注号码封盘了
    blue = random.choice(blue_balls)
    records.append((red, blue))   # 每一注本身也是一个元组 (红球元组, 蓝球)

# enumerate(records, start=1) 同时给出从1开始的序号和每一注内容
# for 循环里的 (red, blue) 是对每一注做元组拆包
for i, (red, blue) in enumerate(records, start=1):
    red_str = ' '.join(f'{b:02d}' for b in red)
    print(f'第{i}注：红球 [{red_str}]  蓝球 [{blue:02d}]')
