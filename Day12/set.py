# ============================================================
# 一、为什么需要集合？
# ============================================================
# 集合（set）专门解决两类问题：
# 1. 自动去重——列表里有重复元素，想快速拿到"不重复的那些"
# 2. 判断归属关系——比如两批数据有没有重叠、差异是什么
# 特点：无序、元素不重复，背后用哈希表存储，查找比列表快很多


# ============================================================
# 二、创建集合
# ============================================================

s1 = {1, 2, 3}                  # 用 {} 直接创建
s2 = set([1, 2, 2, 3, 3, 3])    # 用 set() 把列表转成集合，重复的自动去掉
s3 = set()                       # 空集合，注意：只能用 set()！

print(s1, type(s1))              # {1, 2, 3} <class 'set'>
print(s2)                        # {1, 2, 3}   —— 重复元素自动消失
print(type({}))                  # <class 'dict'> —— 空的 {} 是字典，不是集合！


# ============================================================
# 三、集合是无序的，不能用索引/切片
# ============================================================

# print(s1[0])   # ❌ TypeError: 'set' object is not subscriptable
# 集合不关心元素的顺序，只关心"有没有这个元素"
# 数字集合打印顺序在同一版本Python里通常稳定，但字符串集合每次运行顺序可能不一样
# （下面的 fruits 例子就会体现这一点），这正说明了"集合无序"


# ============================================================
# 四、增删元素
# ============================================================

fruits = {'apple', 'banana'}

fruits.add('pitaya')          # 添加一个元素
print(fruits)                  # 3个元素都在，但顺序不保证，每次运行可能不同

fruits.add('apple')           # 添加已存在的元素——什么也不会发生，不会报错也不会重复
print(fruits)                  # 还是那3个

fruits.discard('banana')      # 删除元素，不存在也不报错
fruits.remove('pitaya')       # 删除元素，如果不存在会报 KeyError（比discard更严格）
print(fruits)                  # {'apple'}

fruits.clear()
print(fruits)                  # set()   —— 空集合打印出来是 set()，不是 {}（避免和空字典混淆）


# ============================================================
# 五、集合运算——集合真正的强项
# ============================================================

a = {1, 2, 3, 4, 5}
b = {4, 5, 6, 7, 8}

print(a | b)     # 并集：合并去重       → {1, 2, 3, 4, 5, 6, 7, 8}
print(a & b)     # 交集：两边都有的     → {4, 5}
print(a - b)     # 差集：只在a、不在b   → {1, 2, 3}
print(b - a)     # 差集：只在b、不在a   → {8, 6, 7}（内容对，只是打印顺序不同）
print(a ^ b)     # 对称差集：只在其中一边（并集减去交集）→ {1, 2, 3, 6, 7, 8}

print(3 in a)                # 成员判断 → True
print({1, 2}.issubset(a))    # 子集判断 → True，{1,2}是a的子集


# ============================================================
# 六、集合推导式（和列表推导式语法一致，把 [] 换成 {}）
# ============================================================

nums = [1, 2, 2, 3, 4, 4, 5]
uniques = {n for n in nums if n % 2 == 0}   # 取出nums里的偶数，顺便自动去重
print(uniques)     # {2, 4}


# ============================================================
# 七、实战：模拟双色球对奖
# 电脑生成一注"开奖号码"，你输入自己买的号码，用交集(&)算出命中了几个红球
# ============================================================

import random

red_balls  = list(range(1, 34))
blue_balls = list(range(1, 17))

winning_red = set(random.sample(red_balls, 6))
winning_blue = random.choice(blue_balls)
print(f'开奖号码：红球 {sorted(winning_red)}  蓝球 {winning_blue:02d}')

text = input('\n请输入你的6个红球号码（空格分隔）：')
my_red = {int(p) for p in text.split() if p.isdigit()}   # 集合推导式：边转int边去重
my_blue = int(input('请输入你的蓝球号码：'))

matched_red = my_red & winning_red     # 交集：你和开奖号码都有的红球
red_hits = len(matched_red)
blue_hit = (my_blue == winning_blue)

print(f'命中红球：{sorted(matched_red)}，共{red_hits}个')
print('蓝球命中！' if blue_hit else '蓝球未命中')

if red_hits == 6 and blue_hit:
    print('恭喜，一等奖！')
elif red_hits == 6:
    print('恭喜，二等奖！')
elif red_hits == 5 and blue_hit:
    print('恭喜，三等奖！')
else:
    print('未中奖，再接再厉～')
