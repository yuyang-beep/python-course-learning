# ============================================================
# 一、变量的四种基本类型
# ============================================================

age = 18            # int    整数
height = 1.75       # float  小数（浮点数）
name = 'Arthur'     # str    字符串
is_student = True   # bool   布尔值（True 或 False）

print(age, height, name, is_student)

# 用 type() 查看变量的类型
print(type(age))         # <class 'int'>
print(type(height))      # <class 'float'>
print(type(name))        # <class 'str'>
print(type(is_student))  # <class 'bool'>


# ============================================================
# 二、变量的基本运算
# ============================================================

a = 45
b = 12
print(a + b)   # 加法 → 57
print(a - b)   # 减法 → 33
print(a * b)   # 乘法 → 540
print(a / b)   # 除法 → 3.75


# ============================================================
# 三、类型转换
# ============================================================

x = '123'          # 字符串 '123'
print(int(x))      # 转成整数 → 123
print(float(x))    # 转成小数 → 123.0

y = 3.99
print(int(y))      # float 转 int，直接截断小数部分 → 3

z = 100
print(str(z))      # 整数转字符串 → '100'
print(chr(z))      # 整数转对应字符 → 'd'


