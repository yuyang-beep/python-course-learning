# ============================================================
# 一、for-in 循环（知道循环次数时用）
# ============================================================
# 语法：
#   for 变量 in range(次数):
#       循环体（缩进4个空格）

print('=== for-in 循环 ===')
for i in range(5):          # i 依次取 0,1,2,3,4
    print(f'第 {i+1} 次')

# range 的三种用法：
# range(5)       → 0,1,2,3,4
# range(1, 6)    → 1,2,3,4,5
# range(1, 10, 2)→ 1,3,5,7,9（步长为2）


# ============================================================
# 二、用循环做计算：1到100求和
# ============================================================

print('\n=== 1到100求和 ===')
total = 0
for i in range(1, 101):   # 1 到 100
    total += i             # 累加，等同于 total = total + i
print(f'1+2+...+100 = {total}')    # → 5050

# 更简洁的写法（用内置函数 sum）：
print(f'用sum函数：{sum(range(1, 101))}')


# ============================================================
# 三、while 循环（不确定循环次数时用）
# ============================================================
# 语法：
#   while 条件:
#       循环体（条件为True时一直执行）

print('\n=== while 循环 ===')
count = 1
while count <= 5:
    print(f'count = {count}')
    count += 1             # 一定要让条件最终变为False，否则死循环！


# ============================================================
# 四、break 和 continue
# ============================================================

print('\n=== break（提前退出循环）===')
for i in range(1, 11):
    if i == 5:
        break              # i 等于5时立刻退出循环
    print(i)               # 只打印 1,2,3,4

print('\n=== continue（跳过本次，继续下一轮）===')
for i in range(1, 11):
    if i % 2 != 0:
        continue           # 奇数跳过
    print(i)               # 只打印 2,4,6,8,10


# ============================================================
# 五、嵌套循环：打印九九乘法表
# ============================================================

print('\n=== 九九乘法表 ===')
for i in range(1, 10):
    for j in range(1, i + 1):
        print(f'{i}×{j}={i*j}', end='\t')
    print()    # 每行结束换行


# ============================================================
# 【练习】：猜数字游戏
# 程序随机生成 1-100 的数字，你来猜，有提示"大了"或"小了"
# ============================================================

import random
print('\n=== 猜数字游戏 ===')
answer = random.randrange(1, 101)   # 随机生成 1~100 的整数
counter = 0

while True:
    counter += 1
    guess = int(input('猜一个 1~100 的整数：'))
    if guess < answer:
        print('再大一点！')
    elif guess > answer:
        print('再小一点！')
    else:
        print(f'猜对了！答案就是 {answer}，你猜了 {counter} 次。')
        break
